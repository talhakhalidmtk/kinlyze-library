// RepoType identifies the primary technology stack of a repository.
type RepoType string

const (
	RepoTypeGeneral  RepoType = "general"
	RepoTypeMuleSoft RepoType = "mulesoft"
)

// DetectRepoType inspects file paths in the repo to identify the stack.
// MuleSoft repos are identified by the presence of .xml Mule config files,
// mule-artifact.json, or src/main/mule/ directory structure.
func DetectRepoType(repoRoot string) RepoType {
	// Check for mule-artifact.json (definitive MuleSoft marker)
	indicators := []string{
		"mule-artifact.json",
		"src/main/mule",
		"src/main/resources/api",
	}
	for _, indicator := range indicators {
		full := filepath.Join(repoRoot, indicator)
		if _, err := os.Stat(full); err == nil {
			return RepoTypeMuleSoft
		}
	}

	// Check git-tracked file list for .xml mule configs
	out, err := runGit([]string{"ls-files", "--", "*.xml"}, repoRoot)
	if err == nil {
		for _, line := range strings.Split(out, "\n") {
			lower := strings.ToLower(line)
			if strings.Contains(lower, "src/main/mule") ||
				strings.Contains(lower, "global-config") ||
				strings.Contains(lower, "mule-artifact") {
				return RepoTypeMuleSoft
			}
		}
	}

	return RepoTypeGeneral
}

// IsBotEmail returns true if the email matches a known bot or CI service pattern.
func IsBotEmail(email string) bool {
	lower := strings.ToLower(email)
	for _, pattern := range botPatterns {
		if strings.Contains(lower, pattern) {
			return true
		}
	}
	return false
}

