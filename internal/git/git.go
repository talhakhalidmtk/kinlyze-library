// dateRegex normalizes timezone offsets without colons (e.g. +0530 → +05:30).
var dateRegex = regexp.MustCompile(`(\d{2}:\d{2}:\d{2}) ([+-]\d{2})(\d{2})$`)

// ── Public types ──────────────────────────────────────────────────────────────

// RepoInfo holds basic repository metadata.
type RepoInfo struct {
	Name         string    `json:"name"`
	Branch       string    `json:"branch"`
	TotalCommits int       `json:"total_commits"`
	FirstCommit  time.Time `json:"first_commit"`
	LastCommit   time.Time `json:"last_commit"`
}

// Commit is a single commit record attached to a file.
type Commit struct {
	SHA          string
	Email        string
	Name         string
	Date         time.Time
	LinesAdded   int
	LinesDeleted int
}

// LinesChanged returns the total lines modified in this commit (adds + deletes).
func (c Commit) LinesChanged() int {
	return c.LinesAdded + c.LinesDeleted
}

// Contributor is a developer with their all-time commit count.
type Contributor struct {
	Name    string
	Email   string
	Commits int
}

// ── Git runner ────────────────────────────────────────────────────────────────

func runGit(args []string, cwd string) (string, error) {
	cmd := exec.Command("git", args...)
	cmd.Dir = cwd
	out, err := cmd.Output()
	if err != nil {
		if exitErr, ok := err.(*exec.ExitError); ok {
			return "", fmt.Errorf("git %s: %s", args[0], string(exitErr.Stderr))
		}
		return "", fmt.Errorf("git not found: %w", err)
	}
	return string(out), nil
}

// ── Repo validation ───────────────────────────────────────────────────────────

// IsGitRepo returns true if path is inside a git repository.
func IsGitRepo(path string) bool {
	_, err := runGit([]string{"rev-parse", "--git-dir"}, path)
	return err == nil
}

// GetRepoRoot returns the absolute path to the repo root.
func GetRepoRoot(path string) (string, error) {
	out, err := runGit([]string{"rev-parse", "--show-toplevel"}, path)
	if err != nil {
		return "", err
	}
	return strings.TrimSpace(out), nil
}

// GetRepoInfo returns basic repo metadata.
func GetRepoInfo(repoRoot string) (RepoInfo, error) {
	info := RepoInfo{}

	// Branch
	branch, err := runGit([]string{"rev-parse", "--abbrev-ref", "HEAD"}, repoRoot)
	if err != nil {
		info.Branch = "unknown"
	} else {
		info.Branch = strings.TrimSpace(branch)
	}

	// Total commits
	countOut, err := runGit([]string{"rev-list", "--count", "HEAD"}, repoRoot)
	if err != nil {
		return info, fmt.Errorf("repository has no commits")
	}
	if _, err := fmt.Sscanf(strings.TrimSpace(countOut), "%d", &info.TotalCommits); err != nil {
		return info, fmt.Errorf("could not parse commit count: %w", err)
	}
	if info.TotalCommits == 0 {
		return info, fmt.Errorf("repository has no commits")
	}

	// Name from root path
	info.Name = filepath.Base(repoRoot)

	// First and last commit dates
	if last, err := runGit([]string{"log", "--format=%ai", "--max-count=1"}, repoRoot); err == nil {
		info.LastCommit = parseDate(strings.TrimSpace(last))
	}
	if first, err := runGit([]string{"log", "--reverse", "--format=%ai", "--max-count=1"}, repoRoot); err == nil {
		info.FirstCommit = parseDate(strings.TrimSpace(first))
	}

	return info, nil
}

// ── File helpers ──────────────────────────────────────────────────────────────

// skipFileStems are filenames (without extension) that should never be analyzed,
// regardless of which directory they live in.
var skipFileStems = map[string]bool{
	"application-types": true, // Mule DataMapper type descriptors — system-generated
}

// ShouldAnalyze returns true if this file path is worth analyzing.
func ShouldAnalyze(path string) bool {
	normalized := filepath.ToSlash(path)
	parts := strings.Split(normalized, "/")

	// Check parent directories
	for _, part := range parts[:max(0, len(parts)-1)] {
		if skipDirs[strings.ToLower(part)] {
			return false
		}
	}

	// Check extension
	ext := strings.ToLower(filepath.Ext(path))
	if skipExtensions[ext] {
		return false
	}

	// Check filename stem
	filename := parts[len(parts)-1]
	stem := strings.TrimSuffix(strings.ToLower(filename), strings.ToLower(filepath.Ext(filename)))
	if skipFileStems[stem] {
		return false
	}

	return len(filename) > 0
}

// IsCriticalFile returns true if the file path matches a critical pattern.
func IsCriticalFile(path string) bool {
	lower := strings.ToLower(path)
	for _, p := range criticalPatterns {
		if strings.Contains(lower, p) {
			return true
		}
	}
	return false
}

// GetModulePath returns the parent directory of a file path.
// "src/payments/processor.go" → "src/payments"
// "main.go"                   → "root"
func GetModulePath(filePath string) string {
	normalized := filepath.ToSlash(filePath)
	idx := strings.LastIndex(normalized, "/")
	if idx < 0 {
		return "root"
	}
	return normalized[:idx]
}

// GetMuleModulePath returns a module key tuned for MuleSoft repos.
// Rules (in order):
//
//  1. Mule flow XML (src/main/mule/) → filename stem
//     src/main/mule/customer-main.xml      → "customer-main"
//
//  2. DataWeave files → "dwl/<stem>"
//     src/main/resources/dwl/order-map.dwl → "dwl/order-map"
//
//  3. Files under src/main/resources/ → strip that prefix so the module
//     name reflects only the sub-folder or filename, never the full path.
//     src/main/resources/config/dev.yaml   → "config"
//     src/main/resources/api/api.raml      → "api"
//     src/main/resources/log4j2.xml        → "log4j2"  (filename stem)
//
//  4. Everything else → standard directory-based grouping
func GetMuleModulePath(filePath string) string {
	normalized := filepath.ToSlash(filePath)
	lower := strings.ToLower(normalized)

	// Rule 1: Mule flow XML files → filename stem
	if strings.HasSuffix(lower, ".xml") && strings.Contains(lower, "src/main/mule") {
		base := filepath.Base(normalized)
		return strings.TrimSuffix(base, filepath.Ext(base))
	}

	// Rule 2: DataWeave files → "dwl/<stem>"
	if strings.HasSuffix(lower, ".dwl") {
		base := filepath.Base(normalized)
		return "dwl/" + strings.TrimSuffix(base, filepath.Ext(base))
	}

	// Rule 3: src/main/resources/** → strip the resources prefix
	const resourcesPrefix = "src/main/resources/"
	if idx := strings.Index(lower, resourcesPrefix); idx >= 0 {
		rel := normalized[idx+len(resourcesPrefix):]
		if rel == "" {
			// File is literally named "resources" — unlikely, but safe fallback
			return "resources"
		}
		// Has a sub-directory: return that directory name
		if slashIdx := strings.Index(rel, "/"); slashIdx > 0 {
			return rel[:slashIdx]
		}
		// File sits directly in src/main/resources/ → use filename stem
		base := filepath.Base(rel)
		return strings.TrimSuffix(base, filepath.Ext(base))
	}

	return GetModulePath(filePath)
}

// MuleSoftLayer represents the API-led connectivity tier of a MuleSoft project.
type MuleSoftLayer string

const (
	MuleSoftLayerExperience MuleSoftLayer = "Experience Layer"
	MuleSoftLayerProcess    MuleSoftLayer = "Process Layer"
	MuleSoftLayerSystem     MuleSoftLayer = "System Layer"
	MuleSoftLayerUnknown    MuleSoftLayer = ""
)

// DetectMuleSoftLayer infers the API-led connectivity layer from the repo name.
// In Mule, teams name repos after the API they expose:
//
//	customer-exp-api  → Experience Layer  (consumer-facing)
//	order-prc-api     → Process Layer     (orchestration)
//	inventory-sys-api → System Layer      (system-of-record adapter)
func DetectMuleSoftLayer(repoName string) MuleSoftLayer {
	name := strings.ToLower(repoName)
	switch {
	case strings.Contains(name, "exp-api") ||
		strings.Contains(name, "experience-api") ||
		strings.Contains(name, "-exp-"):
		return MuleSoftLayerExperience
	case strings.Contains(name, "prc-api") ||
		strings.Contains(name, "process-api") ||
		strings.Contains(name, "-prc-"):
		return MuleSoftLayerProcess
	case strings.Contains(name, "sys-api") ||
		strings.Contains(name, "system-api") ||
		strings.Contains(name, "-sys-"):
		return MuleSoftLayerSystem
	}
	return MuleSoftLayerUnknown
}

// GetContributors returns all-time contributors sorted by commit count.
func GetContributors(repoRoot string) ([]Contributor, error) {
	out, err := runGit([]string{"shortlog", "-sne", "--all", "--no-merges"}, repoRoot)
	if err != nil {
		return nil, err
	}

	re := regexp.MustCompile(`^\s*(\d+)\s+(.+?)\s+<(.+?)>\s*$`)
	var contributors []Contributor

	for _, line := range strings.Split(out, "\n") {
		m := re.FindStringSubmatch(line)
		if m == nil {
			continue
		}
		var count int
		if _, err := fmt.Sscanf(m[1], "%d", &count); err != nil {
			continue
		}
		contributors = append(contributors, Contributor{
			Commits: count,
			Name:    strings.TrimSpace(m[2]),
			Email:   strings.ToLower(strings.TrimSpace(m[3])),
		})
	}

	sort.Slice(contributors, func(i, j int) bool {
		return contributors[i].Commits > contributors[j].Commits
	})
	return contributors, nil
}

// ── Bulk commit loader ────────────────────────────────────────────────────────

// LoadCommitsBulk loads ALL commits for the window in a single git log call.
// Uses --numstat to capture lines added/deleted per file per commit.
// Returns map[filePath][]Commit.
func LoadCommitsBulk(repoRoot string, sinceDays int, progressFn func(string)) (map[string][]Commit, error) {
	if progressFn != nil {
		progressFn(fmt.Sprintf("Running git log (last %d days)...", sinceDays))
	}

	format := commitSep + "%n%H%n%ae%n%an%n%ai"
	args := []string{
		"log",
		fmt.Sprintf("--since=%d days ago", sinceDays),
		fmt.Sprintf("--format=%s", format),
		"--numstat",
		"--no-merges",
		"--diff-filter=AM",
	}

	out, err := runGit(args, repoRoot)
	if err != nil {
		return map[string][]Commit{}, nil // non-fatal — return empty
	}

	if progressFn != nil {
		progressFn("Parsing commit history...")
	}

	return parseBulkLog(out), nil
}

// LoadCommitsWindow loads commits in [untilDays, sinceDays] window for trend comparison.
func LoadCommitsWindow(repoRoot string, sinceDays, untilDays int) map[string][]Commit {
	format := commitSep + "%n%H%n%ae%n%an%n%ai"
	args := []string{
		"log",
		fmt.Sprintf("--since=%d days ago", sinceDays),
		fmt.Sprintf("--format=%s", format),
		"--numstat",
		"--no-merges",
		"--diff-filter=AM",
	}
	if untilDays > 0 {
		args = append(args[:2], append([]string{fmt.Sprintf("--until=%d days ago", untilDays)}, args[2:]...)...)
	}

	out, err := runGit(args, repoRoot)
	if err != nil {
		return map[string][]Commit{}
	}
	return parseBulkLog(out)
}

// parseBulkLog parses git log --numstat output into {filePath: []Commit}.
// State machine: seek → sha → email → name → date → files → repeat
//
// --numstat format per file line: "added\tdeleted\tpath"
// Binary files show as: "-\t-\tpath"
func parseBulkLog(raw string) map[string][]Commit {
	result := make(map[string][]Commit)

	type commitState struct {
		sha   string
		email string
		name  string
		date  time.Time
	}

	var current *commitState
	state := "seek"

	scanner := bufio.NewScanner(strings.NewReader(raw))
	scanner.Buffer(make([]byte, 10*1024*1024), 10*1024*1024)

	for scanner.Scan() {
		line := strings.TrimRight(scanner.Text(), "\r")

		if line == commitSep {
			current = &commitState{}
			state = "sha"
			continue
		}

		if current == nil {
			continue
		}

		switch state {
		case "sha":
			if len(line) >= 8 {
				current.sha = line[:8]
			}
			state = "email"
		case "email":
			current.email = strings.ToLower(strings.TrimSpace(line))
			state = "name"
		case "name":
			current.name = strings.TrimSpace(line)
			state = "date"
		case "date":
			current.date = parseDate(line)
			state = "files"
		case "files":
			if line == "" {
				continue
			}

			// Parse numstat line: "added\tdeleted\tpath"
			parts := strings.SplitN(line, "\t", 3)
			if len(parts) != 3 {
				continue
			}

			filePath := parts[2]

			// Handle rename paths like "{old => new}/file.go" — extract final path
			if strings.Contains(filePath, " => ") {
				filePath = cleanRenamePath(filePath)
			}

			// Parse line counts (binary files show "-" which becomes 0)
			added, _ := strconv.Atoi(parts[0])
			deleted, _ := strconv.Atoi(parts[1])

			if ShouldAnalyze(filePath) && current.email != "" {
				result[filePath] = append(result[filePath], Commit{
					SHA:          current.sha,
					Email:        current.email,
					Name:         current.name,
					Date:         current.date,
					LinesAdded:   added,
					LinesDeleted: deleted,
				})
			}
		}
	}

	return result
}

// cleanRenamePath extracts the destination path from a git rename notation.
// "{old => new}/file.go"  → "new/file.go"
// "dir/{a.go => b.go}"   → "dir/b.go"
func cleanRenamePath(path string) string {
	// Handle {old => new} syntax
	re := regexp.MustCompile(`\{[^}]*\s*=>\s*([^}]*)\}`)
	cleaned := re.ReplaceAllString(path, "$1")
	// Clean up double slashes
	cleaned = strings.ReplaceAll(cleaned, "//", "/")
	cleaned = strings.TrimPrefix(cleaned, "/")
	return cleaned
}

// ── Date parsing ──────────────────────────────────────────────────────────────

var dateFormats = []string{
	"2006-01-02 15:04:05 -0700",
	"2006-01-02 15:04:05 -07:00",
	"2006-01-02T15:04:05-07:00",
	"2006-01-02T15:04:05Z",
}

func parseDate(s string) time.Time {
	s = strings.TrimSpace(s)
	// Normalize "2024-01-15 14:30:00 +0530" timezone format
	s = dateRegex.ReplaceAllString(s, "$1 $2:$3")

	for _, format := range dateFormats {
		if t, err := time.Parse(format, s); err == nil {
			return t
		}
	}
	return time.Time{}
}

