// IsLowImpactPath returns true if the module path is in a low-impact directory.
// These modules are still analyzed but flagged so that risk alerts can distinguish
// "real" operational risk from template/example noise.
func IsLowImpactPath(modulePath string) bool {
	normalized := filepath.ToSlash(strings.ToLower(modulePath))
	parts := strings.Split(normalized, "/")

	for _, part := range parts {
		if lowImpactDirs[part] {
			return true
		}
	}
	return false
}

const commitSep = "<<KINLYZE>>"

